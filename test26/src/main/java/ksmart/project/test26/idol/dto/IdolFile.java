public class IdolFile {
	private int idolFileId;
	private int idolId;
	private String idolFileName;
	private String idolFileExt;
	private long idolFileSize;
	
	public int getIdolFileId() {
		return idolFileId;
	}
	public void setIdolFileId(int idolFileId) {
		this.idolFileId = idolFileId;
	}
	public int getIdolId() {
		return idolId;
	}
	public void setIdolId(int idolId) {
		this.idolId = idolId;
	}
	public String getIdolFileName() {
		return idolFileName;
	}
	public void setIdolFileName(String idolFileName) {
		this.idolFileName = idolFileName;
	}
	public String getIdolFileExt() {
		return idolFileExt;
	}
	public void setIdolFileExt(String idolFileExt) {
		this.idolFileExt = idolFileExt;
	}
	public long getIdolFileSize() {
		return idolFileSize;
	}
	public void setIdolFileSize(long idolFileSize) {
		this.idolFileSize = idolFileSize;
	}
	
	
	
	
	
	
	
}
